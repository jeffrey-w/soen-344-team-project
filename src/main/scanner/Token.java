package main.scanner;

/**
 * The {@code Token} enum specifies the individual syntatic elements of the PIC language. They constitute the smallest
 * elements of the language from which meaning can be derived.
 */
public enum Token {
    NULL,
    AST,
    SLASH,
    PLUS,
    MINUS,
    NOT,
    AND,
    OR,
    EQL,
    NEQ,
    GEQ,
    LSS,
    LEQ,
    GTR,
    PERIOD,
    COMMA,
    COLON,
    OP,
    QUERY,
    LPAREN,
    BECOMES,
    IDENT,
    IF,
    WHILE,
    REPEAT,
    INC,
    DEC,
    ROL,
    ROR,
    NUMBER,
    RPAREN,
    THEN,
    DO,
    SEMICOLON,
    END,
    ELSE,
    ELSIF,
    UNTIL,
    RETURN,
    INT,
    SET,
    BOOL,
    CONST,
    BEGIN,
    PROCED,
    MODULE,
    EOF
}